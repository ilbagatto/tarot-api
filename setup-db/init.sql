CREATE OR REPLACE VIEW nonempty_decks_view AS
SELECT 
    d.id, 
    d.name, 
    d.description
FROM 
    deck d
INNER JOIN 
    card c ON c.deck = d.id
INNER JOIN 
    card_image ci ON ci.card = c.id
GROUP BY 
    d.id;


CREATE OR REPLACE VIEW card_major_view AS
SELECT 
	c.id,
	maj.number,
	maj.name,
	maj.orgname,
	i.path AS image,
	COALESCE(MAX(CASE WHEN mm.position = 'straight' THEN mm.meaning END), '') as straight,
	COALESCE(MAX(CASE WHEN mm.position = 'reverted' THEN mm.meaning END), '') as reverted,
	c.deck,
	mm.source
FROM card_major maj
JOIN card c ON c.id = maj.card
JOIN meaning_major mm ON mm.number = maj.number
JOIN card_image i ON i.card = c.id
GROUP BY c.id, maj.number, maj.name, maj.orgname, i.path, c.deck, mm.source
ORDER BY maj.number;



